export function lookupCost(model: string, material: string): CostEntry | null {
  const m = model.trim().toUpperCase();
  const mat = material.trim().toUpperCase();
  // 精确匹配型号+材质
  const exact = YIFENG_COST_TABLE.find(e => e.model.toUpperCase() === m && e.material.toUpperCase() === mat);
  if (exact) return exact;
  // 只匹配型号（材质为空时）
  if (!mat) {
    return YIFENG_COST_TABLE.find(e => e.model.toUpperCase() === m) ?? null;
  }
  return null;
}